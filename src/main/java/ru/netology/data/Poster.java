package ru.netology.data;

public class Poster {
    private String name;
    private String id;
    private String genre;
    private String imageUrl;

    public Poster (String name, String id, String genre, String imageUrl) {
        this.name = name;
        this.id = id;
        this.genre = genre;
        this.imageUrl = imageUrl;
    }
}
